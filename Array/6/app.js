// 19. Составьте массив дней недели. С помощью цикла for выведите все дни недели кроме выходных дней

let week = ["понедельник", "вторник", "среда", "четверг", "пятница", "суббота","воскресение"]; 
let weekgdays = [];
for (let i = 0; i<week.length; i++) {
    if (week[i] != "суббота" && week[i] != "воскресение"){
        weekgdays.push(week[i])
    }
}
alert(weekgdays)